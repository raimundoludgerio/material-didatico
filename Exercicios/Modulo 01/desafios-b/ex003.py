# Q03 - Conversor de metro, centimentro e milimetro
# Solução desenvolvida por: Maria Estela Hipólito da Silva 
valor = float(input(' digite um valor em metros: '))
centimetros = (valor * 100)
milimetros = (valor * 1000)
print(f'o valor convertido em centimetro é: {centimetros}.')
print(f'o valor convertido em milimetros é: {milimetros}.')